package in.gov.lrit.asp.lritAPI;

public class ShipDetail {
	
	private String mmsi;
	private String imo;
	private String latitude;
    private String longitude;
    private String vesselName;
    private String vesselType;
    private String vesselTypeCode;
    private String vesselTypeCargo;
    private String vesselClass;
    private String country;
    private String destination;
    private String status;
    private String heading;
    
    public ShipDetail() {
    }
    
    public String getMmsi() {
		return mmsi;
	}

	public void setMmsi(String mmsi) {
		this.mmsi = mmsi;
	}
    
    public String getImo() {
		return imo;
	}

	public void setImo(String imo) {
		this.imo = imo;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getVesselName() {
		return vesselName;
	}

	public void setVesselName(String vesselName) {
		this.vesselName = vesselName;
	}

	public String getVesselType() {
		return vesselType;
	}

	public void setVesselType(String vesselType) {
		this.vesselType = vesselType;
	}

	public String getVesselTypeCode() {
		return vesselTypeCode;
	}

	public void setVesselTypeCode(String vesselTypeCode) {
		this.vesselTypeCode = vesselTypeCode;
	}

	public String getVesselTypeCargo() {
		return vesselTypeCargo;
	}

	public void setVesselTypeCargo(String vesselTypeCargo) {
		this.vesselTypeCargo = vesselTypeCargo;
	}

	public String getVesselClass() {
		return vesselClass;
	}

	public void setVesselClass(String vesselClass) {
		this.vesselClass = vesselClass;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getHeading() {
		return heading;
	}

	public void setHeading(String heading) {
		this.heading = heading;
	}
	
	// Creating toString
    @Override
    public String toString()
    {
        return "ShipDetail [Mmsi=" + mmsi
            + ", Imo=" + imo
            + ", Latitude=" + latitude 
            + ", Longitude=" + longitude
            + ", Vessel Name=" + vesselName
            + ", Vessel Type=" + vesselType
            + ", Vessel Type Code=" + vesselTypeCode
            + ", Vessel Type Cargo=" + vesselTypeCargo
            + ", Vessel Class=" + vesselClass
            + ", Country=" + country
            + ", Destination=" + destination
            + ", Status=" + status
            + ", Heading=" + heading
            + "]";
    }

}
